/**
 * 获取文章摘要的提示词
 * @param {string} content 文章内容
 * @returns {Object} 包含system和user提示词的对象
 */
function getArticleSummaryPrompt(content) {
  return {
    system: "你是一个专业的文章摘要助手，擅长提取文章的关键信息并生成简洁明了的摘要。",
    user: `请对以下文章内容生成一个简洁的摘要，不超过200字：\n\n${content}`
  };
}

